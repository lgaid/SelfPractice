package groupPractice;

public class ForLoopTask {

    public static void main(String[] args) {

        for (int j = 0; j < 2; j++) {

            for (int i = 0; i < 2; i++) {

                for (int k = 0; k < 2; k++) {

                    System.out.print("# ");

                }

                System.out.print("# ");

            }
            System.out.println();
        }
    }
}



