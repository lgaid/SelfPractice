package shortVideo_practice_review;

public class EscapeSequences {


    public static void main(String[] args) {

        System.out.println("Hello, How are you? \nI am good" );
        System.out.println("\t Java is fun");
        System.out.println("\\");
        System.out.println("My favorite programming language is \"Java\"");


    }


}
/*
prints like this:
Hello, How are you?
I am good
	 Java is fun
\
My favorite programming language is "Java"

 */




