package day06_ifStatements;

public class WeekdaysJava {

    public static void main(String[] args) {

        int number = 1;

        if (number > number){
            System.out.println("Not Monday");
        }

        if (number < number){
            System.out.println("Not Monday");
        }

        if (number == number){
            System.out.println("Monday");

        }





    }

}
